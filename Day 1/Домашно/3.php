<?php
/*
 * Задача #3
 * Направете приложение, което да събере всички четни числа от 1 до 1337 и да изкара на екрана резултата.
 */

$sum=0;
for($i=1;$i<=1337;$i++) {
	if(($i % 2) == 0) { //знакът % се използва за получаване на остатъка от делене така например 4 % 2 = 0, защото 4/2=2 (няма остатък), но 5 % 2 = 1 защото има остатък 5/2=2 и 1 остатък
		$sum += $i;
	}
}
echo 'Сумата на четните числа от 1 до 1337 е: '.$sum;
?>